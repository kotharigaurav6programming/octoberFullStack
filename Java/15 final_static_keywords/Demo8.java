/*
    Static Keyword : 
    1. VARIABLE
    2. BLOCK
    3. FUNCTION
    4. CLASS 

    Variable : If we make any variable as a static, then in that case  it will become class variable and only single copy is going to create. Which is sharable by every object.

    If we make any variable as a static then in that case we do not need class object to access that variable, static variable is directly access by class name.

    Method : If we make any method as a static, then in that case we do not needs class object to call that static method, static method is directly called by class name.

    Block : static block is a block which executes when a class is loaded in a memory. static block is the block which executes before main method.    
*/