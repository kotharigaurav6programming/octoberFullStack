// example showing the concept of Function in java
class Demo4{
    void printData(){
        System.out.println("This is an example of printData");
    } 
    void showData(){
        System.out.println("This is an example of showData");
    } 
    public static void main(String args[]){
        Demo4 obj = new Demo4();
        obj.printData();
        obj.showData();
    }
}