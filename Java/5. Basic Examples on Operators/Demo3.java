// Program showing the concept of operator
// shorthand operator

class Demo3{
    public static void main(String args[]){
      int a = 625, b = 200;
       a /=25;               // a = a/25;
      System.out.println("a : "+a);  
       b +=10;               // b = b+10;
      System.out.println("b : "+b);  
    }
}